    private void AjouteclientActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AjouteclientActionPerformed
        String prenom = jTextField2.getText();
        String nom = jTextField3.getText();
        String tel = jTextField4.getText();
        String email = jTextField5.getText();
        
        if(prenom.trim().equals("") || nom.trim().equals("") || tel.trim().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Veuillez remplir les champs suivants: Prenom/nom + numero de tel tel", "Champs vides", JOptionPane.WARNING_MESSAGE);
        }
        if (client.ajouterClient(prenom, nom, tel, email)) {
            
            JOptionPane.showMessageDialog(rootPane, "Un nouvrau client a bien été ajouté", "ajouter un client", JOptionPane.INFORMATION_MESSAGE);
        }else{
          JOptionPane.showMessageDialog(rootPane, "Aucun client n'a été ajouté", "Erreur d'ajout d'un client", JOptionPane.INFORMATION_MESSAGE);  
        }        

    }//GEN-LAST:event_AjouteclientActionPerformed

     private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {                                     
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
            
            // get the selected row index
            int rIndex = jTable1.getSelectedRow();
            
            // display data
            jTextField1.setText(model.getValueAt(rIndex, 0).toString());
            jTextField2.setText(model.getValueAt(rIndex, 1).toString());
            jTextField3.setText(model.getValueAt(rIndex, 2).toString());
            jTextField4.setText(model.getValueAt(rIndex, 3).toString());
            jTextField5.setText(model.getValueAt(rIndex, 4).toString());
    }     
