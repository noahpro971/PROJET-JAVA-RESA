    private void jButtonLogInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLogInActionPerformed
        PreparedStatement ps;
        ResultSet rs;
        //get the username and the password
        String username = jTextFieldUsername.getText();
        String password = String.valueOf(jPasswordField.getPassword());
        //check if user is empty
        if(username.trim().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Entrez votre nom d'utilisateur", "Nom d'utilisateur vide", 2);
        }else if(password.trim().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Entrez votre mot de passe", "Mot de passe vide", 2);
        }else{
            Connecto mconn= new Connecto();
            //create the select query
            String selectQuery="SELECT * FROM `utilisateurs` WHERE `username`=? AND `mdp`=?";
            try {
                ps= mconn.createConnection().prepareStatement(selectQuery); 
                ps.setString(1, username);
                ps.setString(2,password);
                rs=ps.executeQuery();
                if(rs.next()){
                    //if this user exist open the main form and close the login form
                    FormPrincipal formPrincipal= new FormPrincipal();
                    formPrincipal.setVisible(true);
                    formPrincipal.pack();
                    formPrincipal.setLocationRelativeTo(null);
                    formPrincipal.setExtendedState(JFrame.MAXIMIZED_BOTH);
                    this.dispose();
                }else{
                    //if the user enter wrong information
                    JOptionPane.showMessageDialog(rootPane, "Mauvais nom d'utilisateur ou mot de passe", "Erreur lors du login", 2);
                }
            } catch (SQLException ex) {
                Logger.getLogger(LogInFormCVV.class.getName()).log(Level.SEVERE, null, ex);
            }
           
        }
        
        
        
    }//GEN-LAST:event_jButtonLogInActionPerformed

